const generateInsight = (disease, location) => {
    if (!disease) return "Analyzing data patterns...";

    const d = disease.toLowerCase();

    if (d.includes('cholera')) {
        return `CareSignal detected a sudden spike in water-borne pathogens in ${location}. Analysis correlates this with recent rainfall and water stagnation reports. Recommended Action: Testing of local water supply.`;
    }
    if (d.includes('dengue')) {
        return `Vector density analysis indicates a 40% increase in mosquito breeding grounds in ${location}. Correlates with humidity levels.`;
    }
    if (d.includes('fever') || d.includes('viral')) {
        return `Community transmission rates have crossed the threshold (R0 > 1.5). Likely seasonal flu variant.`;
    }

    // Default
    return `Anomaly detection algorithms flagged a statistical deviation of ${Math.floor(Math.random() * 20) + 15}% from the 14-day moving average.`;
};

const generateActions = (disease, location) => {
    if (!disease) return [];
    const d = disease.toLowerCase();

    // Water-borne: Cholera, Typhoid, Diarrhea
    if (d.includes('cholera') || d.includes('typhoid') || d.includes('diarrhea')) {
        return [
            `Immediate bacteriological testing of local water sources (taps/wells).`,
            `Distribute Chlorine tablets (Halogen) to households in the affected cluster.`,
            `Inspect sewage pipelines for cross-contamination leaks.`
        ];
    }

    // Vector-borne: Dengue, Malaria, Chikungunya
    if (d.includes('dengue') || d.includes('malaria') || d.includes('chikungunya')) {
        return [
            `Initiate thermal fogging and indoor residual spraying (IRS) within 400m radius.`,
            `Conduct door-to-door larval survey to identify breeding spots.`,
            `Alert local fever clinics to prepare platelet transfusion kits.`
        ];
    }

    // Air-borne: Respiratory Issue, Flu, Viral
    if (d.includes('respiratory') || d.includes('flu') || d.includes('viral')) {
        return [
            `Analyze local AQI (Air Quality Index) and PM2.5 levels for correlation.`,
            `Issue advisory for N95 mask usage in high-density public areas.`,
            `Setup localized fever screening camps at ward entry points.`
        ];
    }

    // Contagious: Measles, Rash
    if (d.includes('measles') || d.includes('rash')) {
        return [
            `Immediate isolation of index cases to prevent community transmission.`,
            `Audit vaccination records (MMR) for children under 5 in the zone.`,
            `Track contact history for the last 7 days.`
        ];
    }

    // Default
    return [
        `Increase active surveillance frequency to hourly intervals.`,
        `Review stock of essential medicines at the nearest PHC.`,
        `Isolate confirmed cases and monitor close contacts.`
    ];
};

